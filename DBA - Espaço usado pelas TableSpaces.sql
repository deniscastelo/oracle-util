SELECT A.DATA_SIZE / 1024 / 1024 / 1024 "DATAFILE",
       B.TEMP_SIZE / 1024 / 1024 / 1024 "TEMPFILE",
       C.REDO_SIZE / 1024 / 1024 / 1024 "LOG",
       D.CF_SIZE / 1024 / 1024 / 1024 "CONTROLFILE",
       E.BCT_SIZE / 1024 / 1024 / 1024 "BLOCK_CHANGE_TRACKING",
       (A.DATA_SIZE + B.TEMP_SIZE + C.REDO_SIZE + D.CF_SIZE + E.BCT_SIZE) / 1024 / 1024 / 1024 TOTAL
  FROM (SELECT SUM(BYTES) DATA_SIZE FROM V$DATAFILE) A,
       (SELECT NVL(SUM(BYTES), 0) TEMP_SIZE FROM V$TEMPFILE) B,
       (SELECT SUM(BYTES) REDO_SIZE FROM V$LOG) C,
       (SELECT SUM(BLOCK_SIZE * FILE_SIZE_BLKS) CF_SIZE FROM V$CONTROLFILE) D,
       (SELECT NVL(BYTES, 0) BCT_SIZE FROM V$BLOCK_CHANGE_TRACKING) E;
